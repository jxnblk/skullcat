const combine = ({
  variation = {},
  kick = {}
}, length = 128) => {
  const config = {}
  const arr = Array.from({ length })
  arr.forEach((a, i) => {
    config[i] = Object.assign({}, {
      variation: variation[i] || null,
      kick: kick[i] || null
    })
  })
  return config
}

